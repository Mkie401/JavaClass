package com.ispan.tutor;

public class Hole02 {

	public static void main(String[] args) {
		byte var1 = 12;
		byte var2;
		System.out.println(var1);
		var2 = 127;
		System.out.println(var2);
		var2 += 3;
		System.out.println(var2);
		short var3 = 32767; // 2^16
		int var4 = 456; // 2^32 => 4G
		long var5 = 12; //2^64
	}

}
